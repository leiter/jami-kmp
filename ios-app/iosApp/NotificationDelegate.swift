import UserNotifications

/// Shows banners while the app is in the foreground and handles notification taps.
class NotificationDelegate: NSObject, UNUserNotificationCenterDelegate {

    static let shared = NotificationDelegate()

    // Show banner + play sound even when the app is in the foreground
    func userNotificationCenter(
        _ center: UNUserNotificationCenter,
        willPresent notification: UNNotification,
        withCompletionHandler completionHandler: @escaping (UNNotificationPresentationOptions) -> Void
    ) {
        completionHandler([.banner, .sound, .badge])
    }

    // Forward taps to the Kotlin navigation layer via the notification userInfo
    func userNotificationCenter(
        _ center: UNUserNotificationCenter,
        didReceive response: UNNotificationResponse,
        withCompletionHandler completionHandler: @escaping () -> Void
    ) {
        // Navigation is handled by the Kotlin side via SharedFlow events;
        // nothing extra is needed here unless we add deep-link routing later.
        completionHandler()
    }
}
