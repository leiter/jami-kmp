/**
 * Handles notification actions for iOS.
 * Implements UNUserNotificationCenterDelegateProtocol to process responses to user notifications.
 */
class IOSNotificationDelegate : NSObject(), UNUserNotificationCenterDelegateProtocol, KoinComponent {

    private val callService: CallService by inject()
    private val conversationFacade: ConversationFacade by inject()

    // Called when a notification is delivered to a foreground app
    override fun userNotificationCenter(
        center: UNUserNotificationCenter,
        willPresentNotification: UNNotification,
        withCompletionHandler: (UNNotificationPresentationOptions) -> Unit
    ) {
        // Decide how to present the notification in the foreground (e.g., sound, alert, badge)
        withCompletionHandler(
            UNNotificationPresentationOptionAlert +
                    UNNotificationPresentationOptionSound +
                    UNNotificationPresentationOptionBadge
        )
    }

    // Called when the user interacts with a notification (e.g., taps, dismisses, or selects an action)
    override fun userNotificationCenter(
        center: UNUserNotificationCenter,
        didReceiveNotificationResponse: UNNotificationResponse,
        withCompletionHandler: () -> Unit
    ) {
        Log.d(TAG, "Notification response received with actionIdentifier: ${didReceiveNotificationResponse.actionIdentifier}")

        val userInfo = didReceiveNotificationResponse.notification.request.content.userInfo
        val accountId = userInfo[KEY_ACCOUNT_ID] as? String
        val conversationId = userInfo[KEY_CONVERSATION_ID] as? String
        val callId = userInfo[KEY_CALL_ID] as? String

        if (accountId == null) {
            Log.w(TAG, "Notification response missing accountId")
            withCompletionHandler()
            return
        }

        CoroutineScope(Dispatchers.Default).launch { // Use Default for UI-related coroutines on iOS
            when (didReceiveNotificationResponse.actionIdentifier) {
                ACTION_ANSWER_CALL -> {
                    if (callId != null) {
                        Log.d(TAG, "Answering call: $callId")
                        callService.acceptCall(callId)
                        // TODO: Dismiss notification programmatically if possible
                    } else {
                        Log.w(TAG, "Answer call action received but callId is null")
                    }
                }
                ACTION_DECLINE_CALL -> {
                    if (callId != null) {
                        Log.d(TAG, "Declining call: $callId")
                        callService.hangupCall(callId)
                        // TODO: Dismiss notification programmatically if possible
                    } else {
                        Log.w(TAG, "Decline call action received but callId is null")
                    }
                }
                ACTION_REPLY_MESSAGE -> {
                    if (conversationId != null && didReceiveNotificationResponse is UNTextInputNotificationResponse) {
                        val replyText = didReceiveNotificationResponse.userText
                        if (replyText.isNotBlank()) {
                            Log.d(TAG, "Replying to conversation $conversationId with: $replyText")
                            conversationFacade.sendMessage(accountId, conversationId, replyText)
                            // TODO: Dismiss notification programmatically if possible
                        } else {
                            Log.w(TAG, "Reply action received but userText is blank")
                        }
                    } else {
                        Log.w(TAG, "Reply action received but conversationId is null or not a text input response")
                    }
                }
                ACTION_MARK_READ -> {
                    if (conversationId != null) {
                        Log.d(TAG, "Marking conversation $conversationId as read")
                        conversationFacade.markConversationAsRead(accountId, Uri(conversationId))
                        // TODO: Dismiss notification programmatically if possible
                    } else {
                        Log.w(TAG, "Mark as read action received but conversationId is null")
                    }
                }
                // Handle notification tap (default action)
                UNNotificationDefaultActionIdentifier -> {
                    // Navigate to appropriate screen (e.g., call screen or chat screen)
                    Log.d(TAG, "Notification tapped. CallId: $callId, ConversationId: $conversationId")
                    // This typically involves routing in the UI layer, which is outside this service.
                }
                // Handle notification dismiss
                UNNotificationDismissActionIdentifier -> {
                    Log.d(TAG, "Notification dismissed.")
                }
                else -> {
                    Log.w(TAG, "Unhandled notification action: ${didReceiveNotificationResponse.actionIdentifier}")
                }
            }
        }
        withCompletionHandler()
    }

    companion object {
        private const val TAG = "IOSNotificationDelegate"

        // Action Identifiers for UNNotificationAction
        const val ACTION_ANSWER_CALL = "ANSWER_CALL"
        const val ACTION_DECLINE_CALL = "DECLINE_CALL"
        const val ACTION_REPLY_MESSAGE = "REPLY_MESSAGE"
        const val ACTION_MARK_READ = "MARK_READ"

        // UserInfo keys
        const val KEY_ACCOUNT_ID = "accountId"
        const val KEY_CONVERSATION_ID = "conversationId"
        const val KEY_CALL_ID = "callId"
    }
}
